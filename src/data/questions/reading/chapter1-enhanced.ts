// ============================================
// 抽题函数 - 按难度比例抽取
// ============================================

export interface QuestionSelectionConfig {
  easyCount: number;    // 简单题数量
  mediumCount: number;  // 中等题数量
  hardCount: number;    // 难题数量
}

/**
 * 从题库中按难度比例随机抽取题目
 * @param passageId 段落ID
 * @param config 抽题配置
 * @returns 抽取的题目列表
 */
export const selectQuestionsByDifficulty = (
  passageId: string,
  config: QuestionSelectionConfig = { easyCount: 2, mediumCount: 2, hardCount: 1 }
): Question[] => {
  const passage = CHAPTER1_PASSAGES.find(p => p.id === passageId);
  if (!passage) return [];

  const easyQuestions = passage.questionPool.filter(q => q.difficulty <= 2);
  const mediumQuestions = passage.questionPool.filter(q => q.difficulty === 3);
  const hardQuestions = passage.questionPool.filter(q => q.difficulty >= 4);

  // 随机打乱并抽取
  const shuffle = (arr: Question[]) => [...arr].sort(() => Math.random() - 0.5);

  const selected = [
    ...shuffle(easyQuestions).slice(0, config.easyCount),
    ...shuffle(mediumQuestions).slice(0, config.mediumCount),
    ...shuffle(hardQuestions).slice(0, config.hardCount)
  ];

  // 再次打乱顺序
  return shuffle(selected);
};

/**
 * 随机抽取指定数量的题目
 * @param passageId 段落ID
 * @param count 题目数量
 * @returns 抽取的题目列表
 */
export const selectRandomQuestions = (passageId: string, count: number): Question[] => {
  const passage = CHAPTER1_PASSAGES.find(p => p.id === passageId);
  if (!passage) return [];

  const shuffled = [...passage.questionPool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

